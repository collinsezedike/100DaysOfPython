@app.route('/register', methods=["POST", "GET"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        # check if email already exists
        if not User.query.filter_by(email=form.email.data).first():
            hashed_password = generate_password_hash(form.password.data, method='pbkdf2:sha256', salt_length=8)
            new_user = User(name=form.name.data, email=form.email.data, password=hashed_password)
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect(url_for("get_all_posts"))

        flash("You have already signed up with that email. Login instead.")
        return redirect(url_for("login"))

    return render_template("register.html", form=form)


@app.route('/login', methods=["POST", "GET"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = form.email.data

        if User.query.filter_by(email=email).first():
            user = User.query.filter_by(email=email).first()

            if check_password_hash(pwhash=user.password, password=form.password.data):
                login_user(user)
                return redirect(url_for("get_all_posts"))

            flash("The password you entered is incorrect, please try again!")
            return render_template("login.html", form=form)

        flash("The email you entered is invalid, please try again!")
        return render_template("login.html", form=form)

    return render_template("login.html", form=form)


