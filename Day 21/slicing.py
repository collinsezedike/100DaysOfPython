piano_keys = ["a", "b", "c", "d", "e", "f", "g"]
piano_tuples = ("do", "re", "mi", "fa", "so", "la", "ti")

# slicing a list
print(piano_keys[2:5])

# slicing to the end 
print(piano_keys[2:])

# slicing from beginning
print(piano_keys[:5])

# slicing with a step
print(piano_keys[::2])

# reverse a list with slicing
print(piano_keys[::-1])

# slicing a tuple
print(piano_tuples[2:5])
